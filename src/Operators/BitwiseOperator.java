package Operators;

public class BitwiseOperator {
	
	public static void main(String[] args) {
		
		System.out.println(15&18);  //  1111
                                     //10010
                                    //--------------
		                            // 00010 
		
		System.out.println(20|40);  //010100
                                    //101000
                                    //------------
                                    //111100
		
		System.out.println(10^30);  //01010
                                    //11110
		                            //---------
		                            //10100


		
	}

}
